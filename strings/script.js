let hello = "Hello"
let world = "World"
let phrase = hello + " " + world + "!"
console.log(phrase);

// Template literals
let phrase2 = `${hello} ${world}!`
console.log("With template literals: ",phrase2);

// Multi-line strings
let text = "Text1... \n " + "Text2..."
console.log(text);

let text2 = `Text1...
Text2...`
console.log("Wiht template literals: ",text2);